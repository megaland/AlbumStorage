package com.usnschool;

import java.io.InputStream;

public class AlbumData {
	private int num;
	private String genre;
	private String singer;
	private String writer;
	private String writerrythm;
	private String relday;
	private String publisher;
	private String planner;
	private String introduce;
	private InputStream imgstream;
	private String albumname;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public String getSinger() {
		return singer;
	}
	public void setSinger(String singer) {
		this.singer = singer;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getWriterrythm() {
		return writerrythm;
	}
	public void setWriterrythm(String writerrythm) {
		this.writerrythm = writerrythm;
	}
	public String getRelday() {
		return relday;
	}
	public void setRelday(String relday) {
		this.relday = relday;
	}
	public String getPublisher() {
		return publisher;
	}
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	public String getPlanner() {
		return planner;
	}
	public void setPlanner(String planner) {
		this.planner = planner;
	}
	public String getIntroduce() {
		return introduce;
	}
	public void setIntroduce(String introduce) {
		this.introduce = introduce;
	}
	public InputStream getImgstream() {
		return imgstream;
	}
	public void setImgstream(InputStream imgstream) {
		this.imgstream = imgstream;
	}
	public String getAlbumname() {
		return albumname;
	}
	public void setAlbumname(String albumname) {
		this.albumname = albumname;
	}
}
